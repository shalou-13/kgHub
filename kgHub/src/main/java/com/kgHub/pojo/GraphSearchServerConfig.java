package com.kgHub.pojo;

public class GraphSearchServerConfig {
	
	public String scheme;
	public String host;
	public String enterPath;
	public String engineClass;
	public boolean state;
	
	
	public String getScheme() {
		return scheme;
	}
	public void setScheme(String scheme) {
		this.scheme = scheme;
	}
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getEnterPath() {
		return enterPath;
	}
	public void setEnterPath(String enterPath) {
		this.enterPath = enterPath;
	}
	public String getEngineClass() {
		return engineClass;
	}
	public void setEngineClass(String engineClass) {
		this.engineClass = engineClass;
	}
	public boolean isState() {
		return state;
	}
	public void setState(boolean state) {
		this.state = state;
	}
	
	
}
